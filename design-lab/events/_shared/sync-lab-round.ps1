# Собрать self-contained CSS + assets для одного раунда design-lab/events/*
# npx serve . отдаёт только текущую папку — пути ../ ломаются (404).
param(
    [Parameter(Mandatory = $true)]
    [string]$RoundDir,
    [switch]$WithLayoutStyles
)

$round = Resolve-Path $RoundDir
$roundLeaf = Split-Path $round -Leaf
$events = Join-Path $round ".."
$out = Join-Path $round "lab-base.css"
$sources = @(
    (Join-Path $events "styles.css"),
    (Join-Path $events "styles-monetka.css")
)

$includeLayout = $WithLayoutStyles.IsPresent -and $WithLayoutStyles
if (-not $includeLayout -and $roundLeaf -ne 'layout-round') {
    $html = Join-Path $round "index.html"
    if ((Test-Path $html) -and (Select-String -Path $html -Pattern 'ev-l3__' -Quiet)) {
        $includeLayout = $true
    }
}

if ($includeLayout) {
    $sources += (Join-Path $events "layout-round\styles.css")
}

$header = @"
/* Auto-generated by sync-lab-round.ps1 — do not edit by hand */
/* Round: $roundLeaf */
/* Sources: $(($sources | ForEach-Object { Split-Path $_ -Leaf }) -join ', ') */

"@
Set-Content $out $header -Encoding utf8
foreach ($src in $sources) {
    if (-not (Test-Path $src)) {
        Write-Error "Missing source: $src"
        exit 1
    }
    Get-Content $src -Raw | Add-Content $out -Encoding utf8
    "`n" | Add-Content $out -Encoding utf8
}

$assets = Join-Path $round "assets"
New-Item -ItemType Directory -Force -Path $assets | Out-Null
$png = Join-Path $events "assets\monetka-mascot.png"
Copy-Item $png (Join-Path $assets "monetka-mascot.png") -Force

Write-Host "OK: $out"
Write-Host "OK: assets/monetka-mascot.png"
